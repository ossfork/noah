def step_tauri_icons() -> None:
    print("[desktop/src-tauri/icons] all plated variants from plated SVG")
    for size, name in _DESKTOP_PNGS + _WINDOWS_STORE + _IOS_ICONS + _ANDROID_PLATED:
        _render_png(PLATED_SVG, size, TAURI_ICONS / name)
    print("[desktop/src-tauri/icons] adaptive-foreground variants from bare SVG")
    for size, name in _ANDROID_BARE:
        _render_png(BARE_SVG, size, TAURI_ICONS / name)


def step_tauri_ico() -> None:
    print("[desktop/src-tauri/icons] icon.ico (multi-resolution from plated)")
    ico_path = TAURI_ICONS / "icon.ico"
    sizes = [16, 32, 48, 256]
    images = []
    for s in sizes:
        buf = io.BytesIO()
        cairosvg.svg2png(url=str(PLATED_SVG), output_width=s, output_height=s, write_to=buf)
        buf.seek(0)
        images.append(Image.open(buf).convert("RGBA"))
    images[0].save(ico_path, format="ICO", sizes=[(s, s) for s in sizes], append_images=images[1:])
    print(f"  → {ico_path}")


def step_tauri_icns() -> None:
    print("[desktop/src-tauri/icons] icon.icns (built via iconutil from plated)")
    import tempfile
    with tempfile.TemporaryDirectory() as td:
        iconset = pathlib.Path(td) / "icon.iconset"
        iconset.mkdir()
        for s in (16, 32, 64, 128, 256, 512, 1024):
            _render_png(PLATED_SVG, s, iconset / f"icon_{s}x{s}.png")
        # @2x retina pairs.
        for s in (16, 32, 128, 256, 512):
            s2 = s * 2
            src = iconset / f"icon_{s2}x{s2}.png"
            if src.exists():
                shutil.copy(src, iconset / f"icon_{s}x{s}@2x.png")
        subprocess.run(
            ["iconutil", "-c", "icns", "-o", str(TAURI_ICONS / "icon.icns"), str(iconset)],
            check=True,
        )
    print(f"  → {TAURI_ICONS / 'icon.icns'}")


def main() -> int:
    if not BARE_SVG.exists() or not PLATED_SVG.exists():
        print(f"ERROR: canonical SVGs missing in {HERE} — run build.py first.", file=sys.stderr)
        return 1
    step_website()
    step_desktop_public()
    step_desktop_assets()
    step_tauri_source()
    step_tauri_icons()
    step_tauri_ico()
    step_tauri_icns()
    print("\nDone. Every consumer of the Noah brand mark has been refreshed.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
