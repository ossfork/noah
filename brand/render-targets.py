#!/usr/bin/env python
"""Render every consumer of the Noah brand mark from the canonical SVGs.

This is the ONLY place icon outputs are produced. Inputs:
  • brand/noah-icon.svg          (bare symbol, transparent corners)
  • brand/noah-icon-plated.svg   (same symbol on rounded white plate)

Outputs are written to:
  • Website (onnoah.app/public/)     — bare SVG copies
  • Desktop public/                  — bare PNG copies (splash + favicon)
  • Desktop src/assets/              — bare SVG copy (used by NoahIcon.tsx)
  • Desktop src-tauri/icons/         — plated PNG/.icns/.ico variants
                                       (dock, taskbar, iOS, Android)

Any other place rendering an icon is a bug — file a fix and add it here.
Run via brand/sync.sh, which also regenerates the source SVGs first.
"""
from __future__ import annotations

import io
import os
import pathlib
import shutil
import subprocess
import sys

import cairosvg
from PIL import Image


HERE = pathlib.Path(__file__).resolve().parent
REPO = HERE.parent
BARE_SVG = HERE / "noah-icon.svg"
PLATED_SVG = HERE / "noah-icon-plated.svg"

# Targets — adjust the ONNOAH path via env if the sibling repo lives elsewhere.
ONNOAH_PUBLIC = pathlib.Path(os.environ.get("ONNOAH_PUBLIC", REPO.parent / "onnoah.app/public"))
DESKTOP = REPO / "apps/desktop"
TAURI_ICONS = DESKTOP / "src-tauri/icons"

WARN = "<!-- AUTO-GENERATED by brand/sync.sh — DO NOT EDIT. Source: noah/brand/build.py -->"


def _copy_svg_with_warning(src: pathlib.Path, dest: pathlib.Path) -> None:
    dest.parent.mkdir(parents=True, exist_ok=True)
    body = src.read_text()
    dest.write_text(f"{WARN}\n{body}")
    print(f"  → {dest}")


def _render_png(src: pathlib.Path, size: int, dest: pathlib.Path) -> None:
    dest.parent.mkdir(parents=True, exist_ok=True)
    cairosvg.svg2png(url=str(src), output_width=size, output_height=size, write_to=str(dest))
    print(f"  → {dest} ({size}×{size})")


def _delete_if_exists(p: pathlib.Path) -> None:
    if p.exists():
        p.unlink()
        print(f"  ✗ removed legacy {p}")


def step_website() -> None:
    print("[website] bare SVG → favicon.svg + noah-icon.svg")
    if not ONNOAH_PUBLIC.is_dir():
        print(f"  ! {ONNOAH_PUBLIC} not found — skipping website copy.")
        return
    _copy_svg_with_warning(BARE_SVG, ONNOAH_PUBLIC / "favicon.svg")
    _copy_svg_with_warning(BARE_SVG, ONNOAH_PUBLIC / "noah-icon.svg")


def step_desktop_public() -> None:
    """The Tauri webview's HTML splash + favicon. Both consume the PLATED
    PNG — the self-contained near-black tile reads on both the dark and
    the light splash background (a bare near-white wrench would vanish on
    the light theme). The HTML applies its own border-radius on top.
    """
    print("[desktop/public] plated PNG → icon-32.png + noah-icon.png")
    pub = DESKTOP / "public"
    _render_png(PLATED_SVG, 32, pub / "icon-32.png")
    _render_png(PLATED_SVG, 256, pub / "noah-icon.png")


def step_desktop_assets() -> None:
    """src/assets/noah-icon.svg is imported by NoahIcon.tsx (the in-app
    logo chip). It gets the PLATED tile so the mark is theme-independent —
    the same near-black app-icon chip in both light and dark mode. We also
    remove the legacy PNG so it can't be re-imported.
    """
    print("[desktop/src/assets] plated SVG → noah-icon.svg (delete legacy PNG)")
    assets = DESKTOP / "src/assets"
    _copy_svg_with_warning(PLATED_SVG, assets / "noah-icon.svg")
    _delete_if_exists(assets / "noah-icon.png")


def step_tauri_source() -> None:
    """The src-tauri/icons/ regen script reads source-aurora.svg. Write
    the plated SVG to that path so the rest of step_tauri_icons can render
    consistent plated variants.
    """
    print("[desktop/src-tauri] plated SVG → source-aurora.svg")
    _copy_svg_with_warning(PLATED_SVG, TAURI_ICONS / "source-aurora.svg")


